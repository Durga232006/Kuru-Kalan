let currentLang = localStorage.getItem('appLang') || 'en';

function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-lang-key]');
    elements.forEach(el => {
        const key = el.getAttribute('data-lang-key');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit')) {
                el.value = translations[lang][key];
            } else if (el.tagName === 'INPUT' && el.placeholder !== undefined && !el.value) {
                el.placeholder = translations[lang][key];
            } else if (el.tagName === 'TEXTAREA' && el.placeholder !== undefined && !el.value) {
                el.placeholder = translations[lang][key];
            } else {
                // Ensure placeholder handling and value inputs have priority
                if (key.startsWith("placeholder-") && typeof el.placeholder !== "undefined") {
                    el.placeholder = translations[lang][key];
                } else if (key.startsWith("val-") && el.tagName === "INPUT") {
                    el.value = translations[lang][key];
                } else {
                    el.innerText = translations[lang][key];
                }
            }
        }
    });
}

function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'ta' : 'en';
    localStorage.setItem('appLang', currentLang);
    applyTranslations(currentLang);
    const btn = document.getElementById('langToggleBtn');
    if (btn) {
        btn.textContent = currentLang === 'en' ? 'தமிழ்' : 'English';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
    const btn = document.getElementById('langToggleBtn');
    if (btn) {
        btn.textContent = currentLang === 'en' ? 'தமிழ்' : 'English';
        btn.addEventListener('click', toggleLanguage);
    }
});

// Hamburger Menu Toggle
function toggleMenu() {
    const nav = document.querySelector('.gov-header nav');
    if (nav) {
        nav.classList.toggle('show');
    }
}
